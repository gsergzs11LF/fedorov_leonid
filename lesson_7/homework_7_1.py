'''
4. Определение максимальной цифры в числе
Запросите у пользователя натуральное число. Используя цикл while, найдите наибольшую цифру в числе.
'''
n = input('Введите число: ')
max_digit = 0 #наше максимальное число которое мы будем выводить
i = 0 #итератор + используется как индекс цифры в строке заданной пользователем
while i < len(n): #цикл будет идти пока итератор-индекс меньше длины строки (если длина строки 5 то индекс 0 - 4)
    digit = int(n[i]) #вводим переменную цифры которая равна конкретному индексу у строки в виде int
    if digit > max_digit: #если эта цифра больше максимальной цифры которая сейчас задана то
        max_digit = digit #делаем эту цифру максимальной
    i += 1 #прибавляем единичку к индексу чтобы идти дальше
print(max_digit)

