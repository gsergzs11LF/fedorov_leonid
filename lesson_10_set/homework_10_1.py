# set_a &= set_b # пересечения А и В вариант 3
# set_a |= set_b # Объединения А и В вариант 3
# set_a -= set_b # Разность А и В вариант 3 c записью в А
# set_a ^= set_b # Симметричная разность А и В. Изменяет список А

print('Пересечение А и В: ', set_a & set_b)
print('Пересечение А и В вариант 2: ', set_a.intersection(set_b))
# print('Пересечение А и В вариант 3: ', set_a)

print('Объединение А и В: ', set_a | set_b)
print('Объединение А и В вариант 2: ', set_a.union(set_b))
