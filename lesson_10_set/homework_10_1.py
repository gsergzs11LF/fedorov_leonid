"""
===============================================
9. Даны множества слов:
text1 = set("программирование")
text2 = set("автоматизация")
Найдите общие буквы в двух словах.
Найдите буквы, которые есть только в первом слове.
Найдите уникальные буквы каждого слова.
===============================================
"""
text1 = set("программирование")
text2 = set("автоматизация")
print('Общие буквы в словах: ', text1.intersection(text2))

# В первом слове есть буквы, которые находятся и во втором слове. Чтобы вывести буквы, которые есть только
# в первом слове нам надо из первого слова удалить буквы которые есть во втором слове.
# Для этого применяется вычетание, которое означает - Удалить элементы, которые есть в вычитаемом множестве
print('Буквы только в первом слове: ', text1.difference(text2))

print('Уникалные буквы каждого слова: ', text1.symmetric_difference(text2)) # оставляем только несовпадающие элементы

