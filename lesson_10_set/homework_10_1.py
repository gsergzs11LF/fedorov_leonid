print("Записаны только на один курс: ", python_students ^ java_students) # записаны ТОЛЬКО на 1 курс значит что есть или в том курсе или в том но не в двух сразу,  надо удалить только пересечения (людей которые есть и в том и в том) и вывести всех остальных
