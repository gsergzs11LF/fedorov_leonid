# print('Разность А и В вариант 3 c записью в А: ', set_a)

print('Симметричная разность А и В: ', set_a ^ set_b) # по сути это объединение множеств с вычитанием пересечений (берем все значения, но удаляем пересечения)
print('Симметричная разность А и В вариант 2: ', set_a.symmetric_difference(set_b))
