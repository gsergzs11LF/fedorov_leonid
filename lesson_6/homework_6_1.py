'''
1. Проверка знака числа
===============================================
Запроси у пользователя число x.
Если x > 0, выведи "Число положительное".
Если x < 0, выведи "Число отрицательное".
В остальных случаях выведи "Число равно нулю".
'''
a = int(input('Введи число:'))
if a > 0:
    print('Число положительное')
elif a < 0:
    print('Число отрицательное')
else:
    print('Это ноль')

'''
2. Проверка на четность
===============================================
Запроси у пользователя целое число x.
Если x четное, выведи "Число четное".
Если x нечетное, выведи "Число нечетное".
'''
x = int(input('Введи целое число '))
if x % 2 == 0:
    print('Число четное')
else:
    print('Число нечетное')

