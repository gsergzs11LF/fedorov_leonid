'''
9. Калькулятор скидки
===============================================
Запроси у пользователя сумму покупки amount.
Если amount >= 5000, выдай скидку 10%.
Если amount >= 1000, выдай скидку 5%.
В остальных случаях без скидки.
Выведи итоговую сумму после скидки.
'''
amount = float(input('Введи сумму своей покупки: '))
if amount >= 5000:
    print(f'Твоя скидка составляет 10% - итоговая сумма: {amount * 0.9}')
elif amount >= 1000:
    print(f'Твоя скидка составляет 5% - итоговая сумма: {amount * 0.95}')
else:
    print(f'У тебя нет скидки - итоговая сумма: {amount}')

'''
10. Определение високосного года
===============================================
Запроси у пользователя год.
Год високосный, если он делится на 4, но не делится на 100 или делится на 400.
Выведи "Год високосный" или "Год не високосный".
'''
year = int(input('Введи какой сейчас год: '))
if (year % 4 == 0 and year % 100 != 0) or year % 400 == 0:
    print('Год високосный')
else:
    print('Год не високосный')


